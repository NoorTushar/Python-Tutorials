# Data Types in Python
# 1. Integers
# 2. String
# 3. Float
# 4. Boolean
# 5. None


# 1. Integers (int)
# includes +ve, -ve and 0 values. all whole numbers
age = 18

# 2. String (str)
# any text wrapped in single, double, or triple quotes
name = "Tushar"

# 3. Float (float)
# any decimal values
price = 14.99

# 4. Boolean (bool)
# True or False
allowed = False

# 5. None (NoneType)
# A variable which has no value inside
hobby = None