# 1. A variable is a name given to memory location in a program
# A variable is something that varies

name = 'Tushar'
age = 16
price = 77.99

# The values are stored from right and side to left-hand side
# As we can see, the value 16 is stored from right hand side
# to the left hand side to the variable named 'age'

print("My name is :", name)
print("My age is :", age)
print("Item price is :", price)

# 2. Checking the variable type.
# whenever we store a value in a variable, python auto detects
# the value type

print(type(name)) # <class 'str'>
print(type(age)) # <class 'int'>
print(type(price)) # <class 'float'>